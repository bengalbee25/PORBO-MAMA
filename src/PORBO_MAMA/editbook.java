    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

          JFileChooser fileChooser;
          if (selectedPDFPath != null && !selectedPDFPath.isEmpty()) {
          fileChooser = new JFileChooser(selectedPDFPath);
          } else {
          fileChooser = new JFileChooser("C:\\Users\\USER\\Desktop");
          }

          FileNameExtensionFilter pdfFilter = new FileNameExtensionFilter("PDF Files", "pdf");
          fileChooser.setFileFilter(pdfFilter);
          int result = fileChooser.showOpenDialog(this);
          if (result == JFileChooser.APPROVE_OPTION) {
          File selectedFile = fileChooser.getSelectedFile();
          String checkPDFpath = selectedFile.getAbsolutePath();
              try {
                  pst = con.prepareStatement("SELECT * FROM user_books WHERE user_id = ? and filepath = ?");
                   pst.setInt(1, userid);
                   pst.setString(2, checkPDFpath);
                   rs = pst.executeQuery();
                   
                   if (rs.next()) {
                   JOptionPane.showMessageDialog(this, "The selected PDF already exists in the database.", "Duplicate File Path", JOptionPane.ERROR_MESSAGE);
                   return; 
                   }
                  if (!pdfFilter.accept(selectedFile)) {
                  JOptionPane.showMessageDialog(this, "Selected file format is not supported. Please select a PDF file.", "File Format Error", JOptionPane.ERROR_MESSAGE);
                  } else {
                  selectedPDFPath = selectedFile.getAbsolutePath();
                  JOptionPane.showMessageDialog(this, "PDF edited successfully: " + selectedPDFPath);
                  }
              } catch (SQLException ex) {
                  Logger.getLogger(editbook.class.getName()).log(Level.SEVERE, null, ex);
                  JOptionPane.showMessageDialog(this, "Error editing PDF: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
              }
           }
          else{
            JOptionPane.showMessageDialog(this, "PDF file not changed", "Error", JOptionPane.ERROR_MESSAGE);
            return;
          }
    }//GEN-LAST:event_jButton3ActionPerformed

