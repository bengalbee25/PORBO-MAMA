    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
          JFileChooser fileChooser;
          if (selectedImagePath != null && imageData != null &&!selectedImagePath.isEmpty()) {
          fileChooser = new JFileChooser(selectedImagePath);
          } else {
          fileChooser = new JFileChooser("C:\\Users\\USER\\Desktop");
          }
          FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("Image Files", "jpg", "jpeg", "png", "gif");
          fileChooser.setFileFilter(imageFilter);
          int result = fileChooser.showOpenDialog(this);
          if (result == JFileChooser.APPROVE_OPTION) {
          File selectedFile = fileChooser.getSelectedFile();
          if (!imageFilter.accept(selectedFile)) {
            JOptionPane.showMessageDialog(this, "Selected file format is not supported. Please select an Image file.", "File Format Error", JOptionPane.ERROR_MESSAGE);
            return;
          }
          selectedImagePath = selectedFile.getAbsolutePath();
          try {
            imageData = Files.readAllBytes(selectedFile.toPath());
            pst = con.prepareStatement("SELECT * FROM user_books WHERE user_id = ?");
            pst.setInt(1, userid);

            rs = pst.executeQuery();
            boolean imageExists = false;
            while (rs.next()) {
                byte[] existingImageData = rs.getBytes("image_data");
                if (Arrays.equals(existingImageData, imageData)) {
                    imageExists = true;
                    break;
                }
            }
            if (imageExists) {
                JOptionPane.showMessageDialog(this, "The selected image already exists in the database.", "Duplicate Image", JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Image edited successfully: " + selectedImagePath);
            }
        } catch (SQLException | IOException ex) {
            Logger.getLogger(editbook.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error editing image: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "No image file selected.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton5ActionPerformed

