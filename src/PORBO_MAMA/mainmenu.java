    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String editbook =JOptionPane.showInputDialog(this,"Enter title of the book to edit");
        if (editbook == null || editbook.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the title of the book", "Error", JOptionPane.ERROR_MESSAGE);
            return; }
        try {
//             pst = con.prepareStatement("select * from user_books where title=? and user_id=?");
//             pst.setString(1, editbook.trim());
             editbook = editbook.trim().replaceAll("\\s+", " ");
             pst = con.prepareStatement("SELECT * FROM user_books WHERE REPLACE(title, ' ', '') = REPLACE(?, ' ', '') AND user_id = ?");
             pst.setString(1, editbook);
             pst.setInt(2, userid);
             rs = pst.executeQuery();
        
        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Book found with the title " + editbook.trim() );
            new editbook(userid,editbook,enteredPassword).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "No book found with the title " + editbook.trim(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        } catch (SQLException ex) {
            Logger.getLogger(mainmenu.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error searching for book to edit: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

