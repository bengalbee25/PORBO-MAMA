    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(mainmenu.this,
                        "Are you sure you want to delete your account?", "Confirm Account Deletion",
                        JOptionPane.YES_NO_OPTION);
                if (choice == JOptionPane.YES_OPTION) {
            try {
                pst = con.prepareStatement("delete from users where user_id=? ");
                pst.setInt(1, userid);
                int rowsAffected = pst.executeUpdate();
                
                 if (rowsAffected > 0) {
           JOptionPane.showMessageDialog(mainmenu.this, "Account deleted successfully");
            new loginsignup().setVisible(true);
            this.dispose();}
            
            } catch (SQLException ex) {
                Logger.getLogger(mainmenu.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error deleting account: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
                    
                } else {
                    JOptionPane.showMessageDialog(mainmenu.this, "Account deletion canceled");
                }
    }//GEN-LAST:event_jMenuItem5ActionPerformed

